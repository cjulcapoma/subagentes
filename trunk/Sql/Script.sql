=======================================================================

/*
AUTOR:	ALONSO MORALES SALAZAR
SP:		MUESTRA LOS REGISTROS DE CATEGORIA X COINCIDENCIAS
*/
CREATE PROCEDURE VT_CATEGORIA_GET03
@criterio VARCHAR(50)
AS
